func TestGenerateMetadataFilesStableAcrossRepeatedGeneration(t *testing.T) {
	root := newFixture(t, map[string]string{
		"go.mod":          "module example.test/app\n\ngo 1.26.2\n",
		"z/controller.go": metadataControllerSource("ZController", "/z", "Zed"),
		"a/controller.go": metadataControllerSource("AController", "/a", "Alpha"),
	})
	controllers, _, err := ParseControllerRoutes(scanFixturePackages(t, root))
	if err != nil {
		t.Fatalf("ParseControllerRoutes returned error: %v", err)
	}

	first, err := GenerateMetadataFiles(controllers)
	if err != nil {
		t.Fatalf("first GenerateMetadataFiles returned error: %v", err)
	}
	second, err := GenerateMetadataFiles(controllers)
	if err != nil {
		t.Fatalf("second GenerateMetadataFiles returned error: %v", err)
	}
	if !reflect.DeepEqual(first, second) {
		t.Fatalf("generated files changed between runs")
	}
	if len(first) != 2 {
		t.Fatalf("files length = %d, want 2", len(first))
	}
	assertGeneratedPath(t, root, first[0], "a/fixture_gest.gen.go")
	assertGeneratedPath(t, root, first[1], "z/fixture_gest.gen.go")
}

