func TestGenerateMetadataFilesGoldenSingleController(t *testing.T) {
	root := newFixture(t, map[string]string{
		"go.mod": "module example.test/app\n\ngo 1.26.2\n",
		"users/controller.go": `package users

import "github.com/r6m/gest"

// @Controller("/users")
// @Tag("Users")
type UserController struct{}

type FindUserRequest struct{}
type FindUserResponse struct{}

// @Get("/:id")
// @Status(200)
// @Status(404)
// @Summary("Find user")
// @Description("Returns a user by ID")
func (c *UserController) Find(ctx *gest.Context, req *FindUserRequest) (*FindUserResponse, error) {
	return nil, nil
}
`,
	})

	files := generateFixtureMetadata(t, root)
	if len(files) != 1 {
		t.Fatalf("files length = %d, want 1", len(files))
	}
	assertGeneratedPath(t, root, files[0], "users/users_gest.gen.go")

	want := `// Code generated by gest. DO NOT EDIT.

package users

import "github.com/r6m/gest"

func (c *UserController) GestController() gest.ControllerDefinition {
	return gest.ControllerDefinition{
		Name:     "UserController",
		BasePath: "/users",
		Tag:      "Users",
		Routes: []gest.RouteDefinition{
			{
				Name:     "Find",
				Method:   "GET",
				Path:     "/:id",
				Handler:  nil,
				Request:  (*FindUserRequest)(nil),
				Response: (*FindUserResponse)(nil),
				Statuses: []int{200, 404},
				Metadata: gest.RouteMetadata{
					Summary:     "Find user",
					Description: "Returns a user by ID",
				},
			},
		},
	}
}
`
	if string(files[0].Content) != want {
		t.Fatalf("generated content:\n%s\nwant:\n%s", files[0].Content, want)
	}
	assertNoInit(t, files[0].Content)
}

