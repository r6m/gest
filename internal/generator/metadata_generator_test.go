func assertGeneratedPath(t *testing.T, root string, file GeneratedFile, want string) {
	t.Helper()

	relative, err := filepath.Rel(root, file.Path)
	if err != nil {
		t.Fatalf("Rel(%q, %q) returned error: %v", root, file.Path, err)
	}
	if filepath.ToSlash(relative) != want {
		t.Fatalf("Path = %q, want %q", filepath.ToSlash(relative), want)
	}
}

func assertNoInit(t *testing.T, content []byte) {
	t.Helper()

	if bytes.Contains(content, []byte("func init(")) {
		t.Fatalf("generated content contains init function:\n%s", content)
	}
}

func metadataControllerSource(typeName string, basePath string, handlerName string) string {
	return `package fixture

import "github.com/r6m/gest"

// @Controller("` + basePath + `")
type ` + typeName + ` struct{}

// @Get("/")
func (c *` + typeName + `) ` + handlerName + `(ctx *gest.Context) error { return nil }
`
}

func runGoTest(t *testing.T, root string) {
	t.Helper()

	command := exec.Command("go", "test", "./...")
	command.Dir = root
	output, err := command.CombinedOutput()
	if err != nil {
		t.Fatalf("go test ./... failed: %v\n%s", err, output)
	}
}

func projectRoot(t *testing.T) string {
	t.Helper()

	workingDir, err := os.Getwd()
	if err != nil {
		t.Fatalf("Getwd returned error: %v", err)
	}
	root, err := filepath.Abs(filepath.Join(workingDir, "..", ".."))
	if err != nil {
		t.Fatalf("Abs returned error: %v", err)
	}
	return root
}

func readFile(t *testing.T, path string) []byte {
	t.Helper()

	content, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("ReadFile(%q) returned error: %v", path, err)
	}
	return content
}

func statFile(t *testing.T, path string) os.FileInfo {
	t.Helper()

	info, err := os.Stat(path)
	if err != nil {
		t.Fatalf("Stat(%q) returned error: %v", path, err)
	}
	return info
}

func assertFileMode(t *testing.T, path string, want os.FileMode) {
	t.Helper()

	got := statFile(t, path).Mode().Perm()
	if got != want {
		t.Fatalf("mode = %s, want %s", got, want)
	}
}

func assertWriteDiagnostic(t *testing.T, diagnostics []Diagnostic, code string, path string) {
	t.Helper()

	if len(diagnostics) != 1 {
		t.Fatalf("diagnostics length = %d, want 1: %#v", len(diagnostics), diagnostics)
	}
	if diagnostics[0].Severity != SeverityError {
		t.Fatalf("Severity = %q, want %q", diagnostics[0].Severity, SeverityError)
	}
	if diagnostics[0].Code != code {
		t.Fatalf("Code = %q, want %q", diagnostics[0].Code, code)
	}
	if diagnostics[0].File != path {
		t.Fatalf("File = %q, want %q", diagnostics[0].File, path)
	}
	if diagnostics[0].Message == "" || diagnostics[0].Hint == "" {
		t.Fatalf("diagnostic = %#v, want message and hint", diagnostics[0])
	}
}
